/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given, 
               char *value, const char *possible_values[],
               const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *stop_char = 0;
  const char *val = value;
  int found;
  char **string_field;
  FIX_UNUSED (field);

  stop_char = 0;
  found = 0;

  if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: `--%s' (`-%c') option given more than once%s\n", 
               package_name, long_opt, short_opt,
               (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: `--%s' option given more than once%s\n", 
               package_name, long_opt,
               (additional_error ? additional_error : ""));
      return 1; /* failure */
    }

  FIX_UNUSED (default_value);
    
  if (field_given && *field_given && ! override)
    return 0;
  if (prev_given)
    (*prev_given)++;
  if (field_given)
    (*field_given)++;
  if (possible_values)
    val = possible_values[found];

  switch(arg_type) {
  case ARG_FLAG:
    *((int *)field) = !*((int *)field);
    break;
  case ARG_INT:
    if (val) *((int *)field) = strtol (val, &stop_char, 0);
    break;
  case ARG_STRING:
    if (val) {
      string_field = (char **)field;
      if (!no_free && *string_field)
        free (*string_field); /* free previous string */
      *string_field = gengetopt_strdup (val);
    }
    break;
  default:
    break;
  };

  /* check numeric conversion */
  switch(arg_type) {
  case ARG_INT:
    if (val && !(stop_char && *stop_char == '\0')) {
      fprintf(stderr, "%s: invalid numeric value: %s\n", package_name, val);
      return 1; /* failure */
    }
    break;
  default:
    ;
  };

  /* store the original value */
  switch(arg_type) {
  case ARG_NO:
  case ARG_FLAG:
    break;
  default:
    if (value && orig_field) {
      if (no_free) {
        *orig_field = value;
      } else {
        if (*orig_field)
          free (*orig_field); /* free previous string */
        *orig_field = gengetopt_strdup (value);
      }
    }
  };

  return 0; /* OK */
}


int
cmdline_parser_internal (
  int argc, char **argv, struct gengetopt_args_info *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */

  int error = 0;
  struct gengetopt_args_info local_args_info;
  
  int override;
  int initialize;
  int check_required;
  int check_ambiguity;
  
  package_name = argv[0];
  
  override = params->override;
  initialize = params->initialize;
  check_required = params->check_required;
  check_ambiguity = params->check_ambiguity;

  if (initialize)
    cmdline_parser_init (args_info);

  cmdline_parser_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = params->print_errors;
  optopt = '?';

  while (1)
    {
      int option_index = 0;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "version",	0, NULL, 'V' },
        { "input",	1, NULL, 'i' },
        { "dat",	0, NULL, 't' },
        { "database",	1, NULL, 'd' },
        { "contexts",	1, NULL, 'c' },
        { "context",	1, NULL, 'e' },
        { "genes",	1, NULL, 'g' },
        { "is_nibble",	1, NULL, 'N' },
        { "networks",	1, NULL, 'n' },
        { "default",	1, NULL, 'b' },
        { "xdsl",	0, NULL, 'x' },
        { "minimal_in",	0, NULL, 'm' },
        { "minimal_out",	1, NULL, 'M' },
        { "lookup",	1, NULL, 'l' },
        { "memmap",	0, NULL, 'p' },
        { "verbosity",	1, NULL, 'v' },
        { 0,  0, 0, 0 }
      };

      c = getopt_long (argc, argv, "hVi:td:c:e:g:N:n:b:xmM:l:pv:", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          cmdline_parser_print_help ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->version_given),
              &(local_args_info.version_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "version", 'V',
              additional_error))
            goto failure;
          cmdline_parser_free (&local_args_info);
          return 0;
        
          break;
        case 'i':	/* DAT/DAB file or context IDs and names.  */
        
        
          if (update_arg( (void *)&(args_info->input_arg), 
               &(args_info->input_orig), &(args_info->input_given),
              &(local_args_info.input_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "input", 'i',
              additional_error))
            goto failure;
        
          break;
        case 't':	/* Use DAT/DAB file input rather than Bayes nets.  */
        
        
          if (update_arg((void *)&(args_info->dat_flag), 0, &(args_info->dat_given),
              &(local_args_info.dat_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "dat", 't',
              additional_error))
            goto failure;
        
          break;
        case 'd':	/* Database directory.  */
        
        
          if (update_arg( (void *)&(args_info->database_arg), 
               &(args_info->database_orig), &(args_info->database_given),
              &(local_args_info.database_given), optarg, 0, ".", ARG_STRING,
              check_ambiguity, override, 0, 0,
              "database", 'd',
              additional_error))
            goto failure;
        
          break;
        case 'c':	/* Context/gene mapping.  */
        
        
          if (update_arg( (void *)&(args_info->contexts_arg), 
               &(args_info->contexts_orig), &(args_info->contexts_given),
              &(local_args_info.contexts_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "contexts", 'c',
              additional_error))
            goto failure;
        
          break;
        case 'e':	/* Context ID to process.  */
        
        
          if (update_arg( (void *)&(args_info->context_arg), 
               &(args_info->context_orig), &(args_info->context_given),
              &(local_args_info.context_given), optarg, 0, 0, ARG_INT,
              check_ambiguity, override, 0, 0,
              "context", 'e',
              additional_error))
            goto failure;
        
          break;
        case 'g':	/* Gene ID to name mapping.  */
        
        
          if (update_arg( (void *)&(args_info->genes_arg), 
               &(args_info->genes_orig), &(args_info->genes_given),
              &(local_args_info.genes_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "genes", 'g',
              additional_error))
            goto failure;
        
          break;
        case 'N':	/* Define whether the database is nibble type..  */
        
        
          if (update_arg( (void *)&(args_info->is_nibble_arg), 
               &(args_info->is_nibble_orig), &(args_info->is_nibble_given),
              &(local_args_info.is_nibble_given), optarg, 0, "1", ARG_INT,
              check_ambiguity, override, 0, 0,
              "is_nibble", 'N',
              additional_error))
            goto failure;
        
          break;
        case 'n':	/* Bayes net directory.  */
        
        
          if (update_arg( (void *)&(args_info->networks_arg), 
               &(args_info->networks_orig), &(args_info->networks_given),
              &(local_args_info.networks_given), optarg, 0, ".", ARG_STRING,
              check_ambiguity, override, 0, 0,
              "networks", 'n',
              additional_error))
            goto failure;
        
          break;
        case 'b':	/* Bayes net for no context.  */
        
        
          if (update_arg( (void *)&(args_info->default_arg), 
               &(args_info->default_orig), &(args_info->default_given),
              &(local_args_info.default_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "default", 'b',
              additional_error))
            goto failure;
        
          break;
        case 'x':	/* Use XDSL files instead of DSL.  */
        
        
          if (update_arg((void *)&(args_info->xdsl_flag), 0, &(args_info->xdsl_given),
              &(local_args_info.xdsl_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "xdsl", 'x',
              additional_error))
            goto failure;
        
          break;
        case 'm':	/* Read stored contexts and minimal Bayes nets.  */
        
        
          if (update_arg((void *)&(args_info->minimal_in_flag), 0, &(args_info->minimal_in_given),
              &(local_args_info.minimal_in_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "minimal_in", 'm',
              additional_error))
            goto failure;
        
          break;
        case 'M':	/* Store contexts and minimal Bayes nets.  */
        
        
          if (update_arg( (void *)&(args_info->minimal_out_arg), 
               &(args_info->minimal_out_orig), &(args_info->minimal_out_given),
              &(local_args_info.minimal_out_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "minimal_out", 'M',
              additional_error))
            goto failure;
        
          break;
        case 'l':	/* Gene name to lookup.  */
        
        
          if (update_arg( (void *)&(args_info->lookup_arg), 
               &(args_info->lookup_orig), &(args_info->lookup_given),
              &(local_args_info.lookup_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "lookup", 'l',
              additional_error))
            goto failure;
        
          break;
        case 'p':	/* Memory map input.  */
        
        
          if (update_arg((void *)&(args_info->memmap_flag), 0, &(args_info->memmap_given),
              &(local_args_info.memmap_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "memmap", 'p',
              additional_error))
            goto failure;
        
          break;
        case 'v':	/* Message verbosity.  */
        
        
          if (update_arg( (void *)&(args_info->verbosity_arg), 
               &(args_info->verbosity_orig), &(args_info->verbosity_given),
              &(local_args_info.verbosity_given), optarg, 0, "5", ARG_INT,
              check_ambiguity, override, 0, 0,
              "verbosity", 'v',
              additional_error))
            goto failure;
        
          break;

        case 0:	/* Long option with no short option */
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */



  if (check_required)
    {
      error += cmdline_parser_required2 (args_info, argv[0], additional_error);
    }

  cmdline_parser_release (&local_args_info);

  if ( error )
    return (EXIT_FAILURE);

  if (optind < argc)
    {
      int i = 0 ;
      int found_prog_name = 0;
      /* whether program name, i.e., argv[0], is in the remaining args
         (this may happen with some implementations of getopt,
          but surely not with the one included by gengetopt) */

      i = optind;
      while (i < argc)
        if (argv[i++] == argv[0]) {
          found_prog_name = 1;
          break;
        }
      i = 0;

      args_info->inputs_num = argc - optind - found_prog_name;
      args_info->inputs =
        (char **)(malloc ((args_info->inputs_num)*sizeof(char *))) ;
      while (optind < argc)
        if (argv[optind++] != argv[0])
          args_info->inputs[ i++ ] = gengetopt_strdup (argv[optind-1]) ;
    }

  return 0;

failure:
  
  cmdline_parser_release (&local_args_info);
  return (EXIT_FAILURE);
}
