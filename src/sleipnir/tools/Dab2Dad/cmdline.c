/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given, 
               char *value, char *possible_values[], const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *stop_char = 0;
  const char *val = value;
  int found;
  char **string_field;

  stop_char = 0;
  found = 0;

  if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: `--%s' (`-%c') option given more than once%s\n", 
               package_name, long_opt, short_opt,
               (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: `--%s' option given more than once%s\n", 
               package_name, long_opt,
               (additional_error ? additional_error : ""));
      return 1; /* failure */
    }

    
  if (field_given && *field_given && ! override)
    return 0;
  if (prev_given)
    (*prev_given)++;
  if (field_given)
    (*field_given)++;
  if (possible_values)
    val = possible_values[found];

  switch(arg_type) {
  case ARG_FLAG:
    *((int *)field) = !*((int *)field);
    break;
  case ARG_INT:
    if (val) *((int *)field) = strtol (val, &stop_char, 0);
    break;
  case ARG_STRING:
    if (val) {
      string_field = (char **)field;
      if (!no_free && *string_field)
        free (*string_field); /* free previous string */
      *string_field = gengetopt_strdup (val);
    }
    break;
  default:
    break;
  };

  /* check numeric conversion */
  switch(arg_type) {
  case ARG_INT:
    if (val && !(stop_char && *stop_char == '\0')) {
      fprintf(stderr, "%s: invalid numeric value: %s\n", package_name, val);
      return 1; /* failure */
    }
    break;
  default:
    ;
  };

  /* store the original value */
  switch(arg_type) {
  case ARG_NO:
  case ARG_FLAG:
    break;
  default:
    if (value && orig_field) {
      if (no_free) {
        *orig_field = value;
      } else {
        if (*orig_field)
          free (*orig_field); /* free previous string */
        *orig_field = gengetopt_strdup (value);
      }
    }
  };

  return 0; /* OK */
}


int
cmdline_parser_internal (int argc, char * const *argv, struct gengetopt_args_info *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */

  int error = 0;
  struct gengetopt_args_info local_args_info;
  
  int override;
  int initialize;
  int check_required;
  int check_ambiguity;
  
  package_name = argv[0];
  
  override = params->override;
  initialize = params->initialize;
  check_required = params->check_required;
  check_ambiguity = params->check_ambiguity;

  if (initialize)
    cmdline_parser_init (args_info);

  cmdline_parser_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = params->print_errors;
  optopt = '?';

  while (1)
    {
      int option_index = 0;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "version",	0, NULL, 'V' },
        { "input",	1, NULL, 'i' },
        { "load",	1, NULL, 'a' },
        { "network",	1, NULL, 'n' },
        { "output",	1, NULL, 'o' },
        { "answers",	1, NULL, 'w' },
        { "directory",	1, NULL, 'd' },
        { "everything",	0, NULL, 'e' },
        { "continuous",	0, NULL, 'c' },
        { "genes",	1, NULL, 'g' },
        { "genex",	1, NULL, 'G' },
        { "lookup1",	1, NULL, 'l' },
        { "lookup2",	1, NULL, 'L' },
        { "lookups",	1, NULL, 't' },
        { "lookupp",	1, NULL, 'T' },
        { "quantize",	0, NULL, 'q' },
        { "paircount",	1, NULL, 'P' },
        { "mask",	1, NULL, 'k' },
        { "memmap",	0, NULL, 'm' },
        { "verbosity",	1, NULL, 'v' },
        { NULL,	0, NULL, 0 }
      };

      c = getopt_long (argc, argv, "hVi:a:n:o:w:d:ecg:G:l:L:t:T:qP:k:mv:", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          cmdline_parser_print_help ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->version_given),
              &(local_args_info.version_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "version", 'V',
              additional_error))
            goto failure;
          cmdline_parser_free (&local_args_info);
          return 0;
        
          break;
        case 'i':	/* Input DAD file.  */
        
          if (args_info->Input_Output_group_counter && override)
            reset_group_Input_Output (args_info);
          args_info->Input_Output_group_counter += 1;
        
          if (update_arg( (void *)&(args_info->input_arg), 
               &(args_info->input_orig), &(args_info->input_given),
              &(local_args_info.input_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "input", 'i',
              additional_error))
            goto failure;
        
          break;
        case 'a':	/* Persistent load DAD file.  */
        
          if (args_info->Input_Output_group_counter && override)
            reset_group_Input_Output (args_info);
          args_info->Input_Output_group_counter += 1;
        
          if (update_arg( (void *)&(args_info->load_arg), 
               &(args_info->load_orig), &(args_info->load_given),
              &(local_args_info.load_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "load", 'a',
              additional_error))
            goto failure;
        
          break;
        case 'n':	/* Input Bayesian network (X)DSL.  */
        
          if (args_info->Input_Output_group_counter && override)
            reset_group_Input_Output (args_info);
          args_info->Input_Output_group_counter += 1;
        
          if (update_arg( (void *)&(args_info->network_arg), 
               &(args_info->network_orig), &(args_info->network_given),
              &(local_args_info.network_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "network", 'n',
              additional_error))
            goto failure;
        
          break;
        case 'o':	/* Output DAD file.  */
        
        
          if (update_arg( (void *)&(args_info->output_arg), 
               &(args_info->output_orig), &(args_info->output_given),
              &(local_args_info.output_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "output", 'o',
              additional_error))
            goto failure;
        
          break;
        case 'w':	/* Answer DAT/DAB file.  */
        
        
          if (update_arg( (void *)&(args_info->answers_arg), 
               &(args_info->answers_orig), &(args_info->answers_given),
              &(local_args_info.answers_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "answers", 'w',
              additional_error))
            goto failure;
        
          break;
        case 'd':	/* Directory with DAB files.  */
        
        
          if (update_arg( (void *)&(args_info->directory_arg), 
               &(args_info->directory_orig), &(args_info->directory_given),
              &(local_args_info.directory_given), optarg, 0, ".", ARG_STRING,
              check_ambiguity, override, 0, 0,
              "directory", 'd',
              additional_error))
            goto failure;
        
          break;
        case 'e':	/* Include pairs without answers.  */
        
        
          if (update_arg((void *)&(args_info->everything_flag), 0, &(args_info->everything_given),
              &(local_args_info.everything_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "everything", 'e',
              additional_error))
            goto failure;
        
          break;
        case 'c':	/* Output continuous values.  */
        
        
          if (update_arg((void *)&(args_info->continuous_flag), 0, &(args_info->continuous_given),
              &(local_args_info.continuous_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "continuous", 'c',
              additional_error))
            goto failure;
        
          break;
        case 'g':	/* Gene inclusion file.  */
        
        
          if (update_arg( (void *)&(args_info->genes_arg), 
               &(args_info->genes_orig), &(args_info->genes_given),
              &(local_args_info.genes_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "genes", 'g',
              additional_error))
            goto failure;
        
          break;
        case 'G':	/* Gene exclusion file.  */
        
        
          if (update_arg( (void *)&(args_info->genex_arg), 
               &(args_info->genex_orig), &(args_info->genex_given),
              &(local_args_info.genex_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "genex", 'G',
              additional_error))
            goto failure;
        
          break;
        case 'l':	/* First lookup gene.  */
        
        
          if (update_arg( (void *)&(args_info->lookup1_arg), 
               &(args_info->lookup1_orig), &(args_info->lookup1_given),
              &(local_args_info.lookup1_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "lookup1", 'l',
              additional_error))
            goto failure;
        
          break;
        case 'L':	/* Second lookup gene.  */
        
        
          if (update_arg( (void *)&(args_info->lookup2_arg), 
               &(args_info->lookup2_orig), &(args_info->lookup2_given),
              &(local_args_info.lookup2_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "lookup2", 'L',
              additional_error))
            goto failure;
        
          break;
        case 't':	/* Lookup gene set.  */
        
        
          if (update_arg( (void *)&(args_info->lookups_arg), 
               &(args_info->lookups_orig), &(args_info->lookups_given),
              &(local_args_info.lookups_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "lookups", 't',
              additional_error))
            goto failure;
        
          break;
        case 'T':	/* Lookup pair set.  */
        
        
          if (update_arg( (void *)&(args_info->lookupp_arg), 
               &(args_info->lookupp_orig), &(args_info->lookupp_given),
              &(local_args_info.lookupp_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "lookupp", 'T',
              additional_error))
            goto failure;
        
          break;
        case 'q':	/* Discretize lookups.  */
        
        
          if (update_arg((void *)&(args_info->quantize_flag), 0, &(args_info->quantize_given),
              &(local_args_info.quantize_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "quantize", 'q',
              additional_error))
            goto failure;
        
          break;
        case 'P':	/* Only count pairs above cutoff.  */
        
        
          if (update_arg( (void *)&(args_info->paircount_arg), 
               &(args_info->paircount_orig), &(args_info->paircount_given),
              &(local_args_info.paircount_given), optarg, 0, "-1", ARG_INT,
              check_ambiguity, override, 0, 0,
              "paircount", 'P',
              additional_error))
            goto failure;
        
          break;
        case 'k':	/* Mask DAT/DAB file.  */
        
        
          if (update_arg( (void *)&(args_info->mask_arg), 
               &(args_info->mask_orig), &(args_info->mask_given),
              &(local_args_info.mask_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "mask", 'k',
              additional_error))
            goto failure;
        
          break;
        case 'm':	/* Memory map input/output.  */
        
        
          if (update_arg((void *)&(args_info->memmap_flag), 0, &(args_info->memmap_given),
              &(local_args_info.memmap_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "memmap", 'm',
              additional_error))
            goto failure;
        
          break;
        case 'v':	/* Message verbosity.  */
        
        
          if (update_arg( (void *)&(args_info->verbosity_arg), 
               &(args_info->verbosity_orig), &(args_info->verbosity_given),
              &(local_args_info.verbosity_given), optarg, 0, "5", ARG_INT,
              check_ambiguity, override, 0, 0,
              "verbosity", 'v',
              additional_error))
            goto failure;
        
          break;

        case 0:	/* Long option with no short option */
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */

  if (args_info->Input_Output_group_counter > 1)
    {
      fprintf (stderr, "%s: %d options of group Input-Output were given. At most one is required.%s\n", argv[0], args_info->Input_Output_group_counter, (additional_error ? additional_error : ""));
      error = 1;
    }
  



  cmdline_parser_release (&local_args_info);

  if ( error )
    return (EXIT_FAILURE);

  if (optind < argc)
    {
      int i = 0 ;
      int found_prog_name = 0;
      /* whether program name, i.e., argv[0], is in the remaining args
         (this may happen with some implementations of getopt,
          but surely not with the one included by gengetopt) */

      i = optind;
      while (i < argc)
        if (argv[i++] == argv[0]) {
          found_prog_name = 1;
          break;
        }
      i = 0;

      args_info->inputs_num = argc - optind - found_prog_name;
      args_info->inputs =
        (char **)(malloc ((args_info->inputs_num)*sizeof(char *))) ;
      while (optind < argc)
        if (argv[optind++] != argv[0])
          args_info->inputs[ i++ ] = gengetopt_strdup (argv[optind-1]) ;
    }

  return 0;

failure:
  
  cmdline_parser_release (&local_args_info);
  return (EXIT_FAILURE);
}
