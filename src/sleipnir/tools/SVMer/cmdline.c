/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given, 
               char *value, char *possible_values[], const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *stop_char = 0;
  const char *val = value;
  int found;
  char **string_field;

  stop_char = 0;
  found = 0;

  if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: `--%s' (`-%c') option given more than once%s\n", 
               package_name, long_opt, short_opt,
               (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: `--%s' option given more than once%s\n", 
               package_name, long_opt,
               (additional_error ? additional_error : ""));
      return 1; /* failure */
    }

  if (possible_values && (found = check_possible_values((value ? value : default_value), possible_values)) < 0)
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: %s argument, \"%s\", for option `--%s' (`-%c')%s\n", 
          package_name, (found == -2) ? "ambiguous" : "invalid", value, long_opt, short_opt,
          (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: %s argument, \"%s\", for option `--%s'%s\n", 
          package_name, (found == -2) ? "ambiguous" : "invalid", value, long_opt,
          (additional_error ? additional_error : ""));
      return 1; /* failure */
    }
    
  if (field_given && *field_given && ! override)
    return 0;
  if (prev_given)
    (*prev_given)++;
  if (field_given)
    (*field_given)++;
  if (possible_values)
    val = possible_values[found];

  switch(arg_type) {
  case ARG_FLAG:
    *((int *)field) = !*((int *)field);
    break;
  case ARG_INT:
    if (val) *((int *)field) = strtol (val, &stop_char, 0);
    break;
  case ARG_FLOAT:
    if (val) *((float *)field) = (float)strtod (val, &stop_char);
    break;
  case ARG_STRING:
    if (val) {
      string_field = (char **)field;
      if (!no_free && *string_field)
        free (*string_field); /* free previous string */
      *string_field = gengetopt_strdup (val);
    }
    break;
  default:
    break;
  };

  /* check numeric conversion */
  switch(arg_type) {
  case ARG_INT:
  case ARG_FLOAT:
    if (val && !(stop_char && *stop_char == '\0')) {
      fprintf(stderr, "%s: invalid numeric value: %s\n", package_name, val);
      return 1; /* failure */
    }
    break;
  default:
    ;
  };

  /* store the original value */
  switch(arg_type) {
  case ARG_NO:
  case ARG_FLAG:
    break;
  default:
    if (value && orig_field) {
      if (no_free) {
        *orig_field = value;
      } else {
        if (*orig_field)
          free (*orig_field); /* free previous string */
        *orig_field = gengetopt_strdup (value);
      }
    }
  };

  return 0; /* OK */
}


int
cmdline_parser_internal (int argc, char * const *argv, struct gengetopt_args_info *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */

  int error = 0;
  struct gengetopt_args_info local_args_info;
  
  int override;
  int initialize;
  int check_required;
  int check_ambiguity;
  
  package_name = argv[0];
  
  override = params->override;
  initialize = params->initialize;
  check_required = params->check_required;
  check_ambiguity = params->check_ambiguity;

  if (initialize)
    cmdline_parser_init (args_info);

  cmdline_parser_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = params->print_errors;
  optopt = '?';

  while (1)
    {
      int option_index = 0;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "version",	0, NULL, 'V' },
        { "input",	1, NULL, 'i' },
        { "output",	1, NULL, 'o' },
        { "model",	1, NULL, 'm' },
        { "pcl",	0, NULL, 'p' },
        { "binary",	1, NULL, 'b' },
        { "genewise",	0, NULL, 'w' },
        { "genel",	1, NULL, 'l' },
        { "genes",	1, NULL, 'g' },
        { "genex",	1, NULL, 'G' },
        { "genet",	1, NULL, 'c' },
        { "kernel",	1, NULL, 'k' },
        { "cache",	1, NULL, 'e' },
        { "tradeoff",	1, NULL, 'C' },
        { "gamma",	1, NULL, 'M' },
        { "degree",	1, NULL, 'd' },
        { "alphas",	1, NULL, 'a' },
        { "iterations",	1, NULL, 't' },
        { "skip",	1, NULL, 's' },
        { "random",	1, NULL, 'r' },
        { "verbosity",	1, NULL, 'v' },
        { NULL,	0, NULL, 0 }
      };

      c = getopt_long (argc, argv, "hVi:o:m:pb:wl:g:G:c:k:e:C:M:d:a:t:s:r:v:", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          cmdline_parser_print_help ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->version_given),
              &(local_args_info.version_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "version", 'V',
              additional_error))
            goto failure;
          cmdline_parser_free (&local_args_info);
          return 0;
        
          break;
        case 'i':	/* Input answer DAT/DAB file.  */
        
        
          if (update_arg( (void *)&(args_info->input_arg), 
               &(args_info->input_orig), &(args_info->input_given),
              &(local_args_info.input_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "input", 'i',
              additional_error))
            goto failure;
        
          break;
        case 'o':	/* Output prediction DAT/DAB file.  */
        
        
          if (update_arg( (void *)&(args_info->output_arg), 
               &(args_info->output_orig), &(args_info->output_given),
              &(local_args_info.output_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "output", 'o',
              additional_error))
            goto failure;
        
          break;
        case 'm':	/* SVM model file or directory.  */
        
        
          if (update_arg( (void *)&(args_info->model_arg), 
               &(args_info->model_orig), &(args_info->model_given),
              &(local_args_info.model_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "model", 'm',
              additional_error))
            goto failure;
        
          break;
        case 'p':	/* PCL input mode.  */
        
        
          if (update_arg((void *)&(args_info->pcl_flag), 0, &(args_info->pcl_given),
              &(local_args_info.pcl_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "pcl", 'p',
              additional_error))
            goto failure;
        
          break;
        case 'b':	/* Input binary training file.  */
        
        
          if (update_arg( (void *)&(args_info->binary_arg), 
               &(args_info->binary_orig), &(args_info->binary_given),
              &(local_args_info.binary_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "binary", 'b',
              additional_error))
            goto failure;
        
          break;
        case 'w':	/* Learn per-gene SVMs for pairwise predictions.  */
        
        
          if (update_arg((void *)&(args_info->genewise_flag), 0, &(args_info->genewise_given),
              &(local_args_info.genewise_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "genewise", 'w',
              additional_error))
            goto failure;
        
          break;
        case 'l':	/* Gene skip file for per-gene SVMs.  */
        
        
          if (update_arg( (void *)&(args_info->genel_arg), 
               &(args_info->genel_orig), &(args_info->genel_given),
              &(local_args_info.genel_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "genel", 'l',
              additional_error))
            goto failure;
        
          break;
        case 'g':	/* Gene inclusion file.  */
        
        
          if (update_arg( (void *)&(args_info->genes_arg), 
               &(args_info->genes_orig), &(args_info->genes_given),
              &(local_args_info.genes_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "genes", 'g',
              additional_error))
            goto failure;
        
          break;
        case 'G':	/* Gene exclusion file.  */
        
        
          if (update_arg( (void *)&(args_info->genex_arg), 
               &(args_info->genex_orig), &(args_info->genex_given),
              &(local_args_info.genex_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "genex", 'G',
              additional_error))
            goto failure;
        
          break;
        case 'c':	/* Term inclusion file.  */
        
        
          if (update_arg( (void *)&(args_info->genet_arg), 
               &(args_info->genet_orig), &(args_info->genet_given),
              &(local_args_info.genet_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "genet", 'c',
              additional_error))
            goto failure;
        
          break;
        case 'k':	/* SVM kernel function.  */
        
        
          if (update_arg( (void *)&(args_info->kernel_arg), 
               &(args_info->kernel_orig), &(args_info->kernel_given),
              &(local_args_info.kernel_given), optarg, cmdline_parser_kernel_values, "linear", ARG_STRING,
              check_ambiguity, override, 0, 0,
              "kernel", 'k',
              additional_error))
            goto failure;
        
          break;
        case 'e':	/* SVM cache size.  */
        
        
          if (update_arg( (void *)&(args_info->cache_arg), 
               &(args_info->cache_orig), &(args_info->cache_given),
              &(local_args_info.cache_given), optarg, 0, "40", ARG_INT,
              check_ambiguity, override, 0, 0,
              "cache", 'e',
              additional_error))
            goto failure;
        
          break;
        case 'C':	/* Classification tradeoff.  */
        
        
          if (update_arg( (void *)&(args_info->tradeoff_arg), 
               &(args_info->tradeoff_orig), &(args_info->tradeoff_given),
              &(local_args_info.tradeoff_given), optarg, 0, 0, ARG_FLOAT,
              check_ambiguity, override, 0, 0,
              "tradeoff", 'C',
              additional_error))
            goto failure;
        
          break;
        case 'M':	/* RBF gamma.  */
        
        
          if (update_arg( (void *)&(args_info->gamma_arg), 
               &(args_info->gamma_orig), &(args_info->gamma_given),
              &(local_args_info.gamma_given), optarg, 0, "1", ARG_FLOAT,
              check_ambiguity, override, 0, 0,
              "gamma", 'M',
              additional_error))
            goto failure;
        
          break;
        case 'd':	/* Polynomial degree.  */
        
        
          if (update_arg( (void *)&(args_info->degree_arg), 
               &(args_info->degree_orig), &(args_info->degree_given),
              &(local_args_info.degree_given), optarg, 0, "3", ARG_INT,
              check_ambiguity, override, 0, 0,
              "degree", 'd',
              additional_error))
            goto failure;
        
          break;
        case 'a':	/* SVM alphas file.  */
        
        
          if (update_arg( (void *)&(args_info->alphas_arg), 
               &(args_info->alphas_orig), &(args_info->alphas_given),
              &(local_args_info.alphas_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "alphas", 'a',
              additional_error))
            goto failure;
        
          break;
        case 't':	/* SVM iterations.  */
        
        
          if (update_arg( (void *)&(args_info->iterations_arg), 
               &(args_info->iterations_orig), &(args_info->iterations_given),
              &(local_args_info.iterations_given), optarg, 0, "100000", ARG_INT,
              check_ambiguity, override, 0, 0,
              "iterations", 't',
              additional_error))
            goto failure;
        
          break;
        case 's':	/* Columns to skip in input PCLs.  */
        
        
          if (update_arg( (void *)&(args_info->skip_arg), 
               &(args_info->skip_orig), &(args_info->skip_given),
              &(local_args_info.skip_given), optarg, 0, "2", ARG_INT,
              check_ambiguity, override, 0, 0,
              "skip", 's',
              additional_error))
            goto failure;
        
          break;
        case 'r':	/* Seed random generator.  */
        
        
          if (update_arg( (void *)&(args_info->random_arg), 
               &(args_info->random_orig), &(args_info->random_given),
              &(local_args_info.random_given), optarg, 0, "0", ARG_INT,
              check_ambiguity, override, 0, 0,
              "random", 'r',
              additional_error))
            goto failure;
        
          break;
        case 'v':	/* Message verbosity.  */
        
        
          if (update_arg( (void *)&(args_info->verbosity_arg), 
               &(args_info->verbosity_orig), &(args_info->verbosity_given),
              &(local_args_info.verbosity_given), optarg, 0, "5", ARG_INT,
              check_ambiguity, override, 0, 0,
              "verbosity", 'v',
              additional_error))
            goto failure;
        
          break;

        case 0:	/* Long option with no short option */
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */




  cmdline_parser_release (&local_args_info);

  if ( error )
    return (EXIT_FAILURE);

  if (optind < argc)
    {
      int i = 0 ;
      int found_prog_name = 0;
      /* whether program name, i.e., argv[0], is in the remaining args
         (this may happen with some implementations of getopt,
          but surely not with the one included by gengetopt) */

      i = optind;
      while (i < argc)
        if (argv[i++] == argv[0]) {
          found_prog_name = 1;
          break;
        }
      i = 0;

      args_info->inputs_num = argc - optind - found_prog_name;
      args_info->inputs =
        (char **)(malloc ((args_info->inputs_num)*sizeof(char *))) ;
      while (optind < argc)
        if (argv[optind++] != argv[0])
          args_info->inputs[ i++ ] = gengetopt_strdup (argv[optind-1]) ;
    }

  return 0;

failure:
  
  cmdline_parser_release (&local_args_info);
  return (EXIT_FAILURE);
}
